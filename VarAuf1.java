public class VarAuf1 {
    public static void main(String[] args) {
        char buchstabe = 'A';      
        int zahl = 100;            
        double kommazahl = 1.344;  
        long grosseZahl = 100200300L; 
        int negativeZahl = -12;    
        // Ausgabe
        System.out.println("Char: " + buchstabe);
        System.out.println("Int: " + zahl);
        System.out.println("Double: " + kommazahl);
        System.out.println("Long: " + grosseZahl);
        System.out.println("Negative Int: " + negativeZahl);
    }
}
